def _score_bar(score, out_of=10) -> str:
    """Turn a numeric score into a visual progress bar."""
    try:
        s = float(score)
    except (TypeError, ValueError):
        return "N/A"
    filled = round(s)
    empty  = out_of - filled
    bar    = "█" * filled + "░" * empty
    return f"`{bar}` {s}/{out_of}"


def _eeat_badge(score) -> str:
    try:
        s = float(score)
    except (TypeError, ValueError):
        return "⬜ N/A"
    if s >= 7.5: return "🟢 Strong"
    if s >= 5.0: return "🟡 Moderate"
    return "🔴 Weak"


def _priority_tag(score) -> str:
    try:
        s = float(score)
    except (TypeError, ValueError):
        return ""
    if s < 4.0: return " 🚨 **CRITICAL**"
    if s < 6.0: return " ⚠️ **HIGH PRIORITY**"
    return " ✅ Acceptable"


def _clean_excerpt(text: str, max_words: int = 40) -> str:
    """Return a clean, sentence-trimmed excerpt."""
    if not text:
        return ""
    words = text.split()
    if len(words) <= max_words:
        return text.strip()
    truncated = " ".join(words[:max_words])
    # Try to end on a sentence boundary
    for punct in [". ", "! ", "? "]:
        idx = truncated.rfind(punct)
        if idx > max_words * 3:  # At least half the excerpt
            return truncated[:idx + 1].strip()
    return truncated.strip() + "…"


def _get_page_excerpt(page: dict, max_words: int = 40) -> str:
    """Extract a meaningful excerpt from page body text."""
    body = page.get("body_excerpt", "") or page.get("full_text", "")
    return _clean_excerpt(body, max_words)


def _severity_emoji(count: int) -> str:
    if count == 0: return "✅"
    if count <= 2: return "⚠️"
    return "🔴"


# ─────────────────────────────────────────────────────────────────────────────
# Public entry point
# ─────────────────────────────────────────────────────────────────────────────

def write_markdown_report(
    domain: str,
    client_data: dict,
    competitor_data: dict,
    gap_results: dict,
    eeat_scores: dict,
    technical: dict,
    output_dir: str = "reports",
) -> str:
    timestamp  = datetime.now().strftime("%Y%m%d_%H%M")
    safe_domain = domain.replace(".", "_").replace("/", "_")
    filepath   = Path(output_dir) / f"{safe_domain}_{timestamp}_audit.md"
    filepath.parent.mkdir(parents=True, exist_ok=True)

    sections = []
    sections += _section_header(domain)
    sections += _section_executive_summary(domain, client_data, competitor_data, eeat_scores, technical)
    sections += _section_eeat(eeat_scores, client_data)
    sections += _section_content_gaps(gap_results, client_data, competitor_data)
    sections += _section_technical(technical, client_data)
    sections += _section_next_steps(eeat_scores, gap_results, technical)
    sections += _section_footer()

    filepath.write_text("\n".join(sections), encoding="utf-8")
    return str(filepath)


